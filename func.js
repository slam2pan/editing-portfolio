function montageFunc() {
	var x = document.getElementById("Montages");
	var y = document.getElementById("IRL");
	x.style.display = "block";
	y.style.display = "none";
}

function IRLFunc() {
	var x = document.getElementById("Montages");
	var y = document.getElementById("IRL");
	x.style.display = "none";
	y.style.display = "block";
}
